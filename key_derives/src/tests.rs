use curve25519_dalek::{Scalar, constants::ED25519_BASEPOINT_TABLE};
use zeroize::Zeroizing;

use crate::LegacyWallet;

#[test]
fn legacy() {
  let seed: Zeroizing<[u8; 32]> = Zeroizing::new([
    0xb2, 0x16, 0x80, 0xc7, 0x97, 0x8c, 0x66, 0xe5, 0x9d, 0xa9, 0xe0, 0xeb, 0xb9, 0x7f, 0xbb, 0x83,
    0xe3, 0xfb, 0x65, 0x5f, 0xf6, 0x25, 0x8d, 0x0d, 0xd5, 0xb5, 0x07, 0x7d, 0x6f, 0x28, 0x13, 0x0c,
  ]);

  let view_key: [u8; 32] = [
    0x29, 0xbf, 0xa3, 0x4e, 0x58, 0x12, 0xfd, 0x11, 0x24, 0xdb, 0x86, 0x3c, 0xb2, 0xd9, 0xf0, 0x73,
    0xcd, 0xa2, 0x10, 0xa6, 0x86, 0x3c, 0xa5, 0x50, 0x2f, 0x68, 0x2e, 0x85, 0xcf, 0x31, 0x5d, 0x07,
  ];

  let wallet = LegacyWallet::new(&seed);

  assert_eq!(wallet.priv_spend, Zeroizing::new(Scalar::from_bytes_mod_order(*seed)));
  assert_eq!(wallet.priv_view, Zeroizing::new(Scalar::from_bytes_mod_order(view_key)));
  assert_eq!(wallet.pub_spend, &Scalar::from_bytes_mod_order(*seed) * ED25519_BASEPOINT_TABLE);
  assert_eq!(wallet.pub_view, &Scalar::from_bytes_mod_order(view_key) * ED25519_BASEPOINT_TABLE);
}
